public class Demo4{
    public static void main(String[] args){
        /*data types & literals */

        // floating point numbers

        //decimal -> dayta type = double
        System.out.println(10.); 
        System.out.println(.25); 
        System.out.println(10.25); 
        System.out.println(10.25e2); 
        System.out.println(10.25E2); 

        System.out.println(0x1.p1); 
        System.out.println(0x11.P2);
        System.out.println(0xA.1p2); 
        System.out.println(0XA.FP2);

        //decimal -> dayta type = float
        System.out.println(10.f); 
        System.out.println(.25F); 

        System.out.println(0x1.p1f); 
        System.out.println(0x11.P2F);


    }
}